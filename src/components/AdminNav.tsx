export default function AdminNav() {
  const [open, setOpen] = useState(false);
  const [negocio, setNegocio] = useState("");
  const router = useRouter();

  useEffect(() => { setOpen(false); }, [router.pathname]);

  useEffect(() => {
    fetch("/api/config-publica").then(r => r.json()).then(d => {
      if (d.nombre_negocio) setNegocio(d.nombre_negocio);
    }).catch(() => {});
  }, []);

  async function logout() {
    await fetch("/api/admin/auth", { method: "DELETE" });
    router.push("/");
  }

  const sidebarContent = (
    <>
      {/* Cabecera */}
      <div className="flex items-center justify-between px-4 py-4 border-b border-gray-800">
        <span className="text-white font-bold text-base">{negocio || "Estudio"}</span>
        {/* Botón cerrar — solo visible en mobile */}
        <button
          onClick={() => setOpen(false)}
          aria-label="Cerrar menú"
          className="sm:hidden text-gray-400 hover:text-white p-1.5 rounded-lg hover:bg-gray-800 transition-colors"
        >
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M6 18 18 6M6 6l12 12" />
          </svg>
        </button>
      </div>

      {/* Links */}
      <nav className="flex-1 overflow-y-auto p-3 space-y-0.5">
        {LINKS.map(({ href, label, match, icon }) => {
          const active = match(router.pathname);
          return (
            <Link
              key={href}
              href={href}
              className={`flex items-center gap-3 px-3 py-3 rounded-xl text-sm font-medium transition-colors ${
                active
                  ? "bg-blue-600/20 text-blue-400"
                  : "text-gray-400 hover:text-white hover:bg-gray-800"
              }`}
            >
              {icon}
              {label}
            </Link>
          );
        })}
      </nav>

      {/* Cerrar sesión */}
      <div className="p-3 border-t border-gray-800">
        <button
          onClick={logout}
          className="w-full flex items-center gap-3 px-3 py-3 rounded-xl text-sm font-medium text-red-400 hover:text-red-300 hover:bg-gray-800 transition-colors"
        >
          <svg className="w-5 h-5 shrink-0" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M8.25 9V5.25A2.25 2.25 0 0 1 10.5 3h6a2.25 2.25 0 0 1 2.25 2.25v13.5A2.25 2.25 0 0 1 16.5 21h-6a2.25 2.25 0 0 1-2.25-2.25V15M12 9l3 3m0 0-3 3m3-3H2.25" />
          </svg>
          Cerrar sesión
        </button>
      </div>
    </>
  );

  return (
    <>
      {/* ── MOBILE: hamburguesa + overlay + drawer deslizante ── */}
      <button
        onClick={() => setOpen(true)}
        aria-label="Abrir menú"
        className="sm:hidden fixed top-3.5 left-4 z-40 p-2 rounded-xl bg-gray-900 border border-gray-800 text-gray-400 hover:text-white hover:bg-gray-800 active:bg-gray-700 transition-colors"
      >
        <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
          <path strokeLinecap="round" strokeLinejoin="round" d="M3.75 6.75h16.5M3.75 12h16.5m-16.5 5.25h16.5" />
        </svg>
      </button>

      {open && (
        <div
          className="sm:hidden fixed inset-0 z-50 bg-black/60"
          onClick={() => setOpen(false)}
        />
      )}

      {/* Drawer mobile: desliza, z-[60] para quedar sobre el overlay */}
      <div
        className={`sm:hidden fixed top-0 left-0 h-full w-64 z-[60] bg-gray-900 border-r border-gray-800 flex flex-col transform transition-transform duration-200 ease-in-out ${
          open ? "translate-x-0" : "-translate-x-full"
        }`}
      >
        {sidebarContent}
      </div>

      {/* ── DESKTOP: sidebar fijo siempre visible ── */}
      <div className="hidden sm:flex fixed top-0 left-0 h-full w-64 z-30 bg-gray-900 border-r border-gray-800 flex-col">
        {sidebarContent}
      </div>
    </>
  );
}
